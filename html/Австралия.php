    <p>Австралия — государство на материке Австралия. Вместе с близлежащим островом Тасмания образует Австралийский Союз. На севере омывается Тиморским, Арафурским морями и проливом Торреса, на востоке — Коралловым и Тасмановым морями, на юге — проливом Басса и Индийским океаном, на западе — Индийским океаном. Союзу принадлежат также острова Картье, Ашмор, остров Рождества, Кокосовые острова, острова Херд, Мак-дональд, Норфолк.</p>
