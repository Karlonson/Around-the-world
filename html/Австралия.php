    <h4> Климат</h4>
    <br>
    <p>Климат в Австралии отличается в зависимости от региона: на севере — тропический, на юге — умеренный. Тропический регион имеет два времени года: жаркий и влажный период (в феврале и марте) и теплый сухой период. Некоторые регионы ежегодно переживают периоды засухи или паводков. Средние летние температуры +20 - 28°С, зимние + 12 - 24°С, наибольшие морозы достигают - 6°С и лишь в Австралийских Альпах - 22 °С. В летние месяцы дожди незначительны.

    </p>
<br>
<p>Более 1000 мм осадков в год получают северная, восточная и южная окраины материка, но во внутренних районах (1/2 площади) — не более 250 мм в год. Май — август — идеальное время для поездки в Австралию, сентябрь — декабрь — пик туристического сезона.</p>
<br>
<h4>Растительный мир</h4>
<br>
<img src="../img/img112.jpg" class="Выборг">
<br>
<p>Природа Австралии исключительно разнообразна. Она очень контрастна по переходам из области пустынь в более плодородные и богатые водой области. Внутренняя часть Австралии занята пустынями. Это Большая песчаная пустыня, Большая пустыня Виктория, пустыня Гибсона. На Севере, Востоке, Юго-Западе пустыни переходят в саванны, которые сменяются лесами из эвкалиптов, пальм, древовидных папоротников.</p>
<br>
<p>Во всем мире известна целительная сила эвкалиптовых лесов. Животный мир достаточно разнообразен, некоторые виды животных характерны только для Австралии - например медведь коала, кенгуру, страус. В пустынях и засушливых районах растут густые, почти непроходимые заросли кустарника ("скрэб").</p>
<br>
<p>В зоне большего увлажнения распространены вечнозеленые тропические леса из мощных высоких деревьев - эвкалиптов (свыше 500 видов), фикусов, пальм, панданусов и прочих.</p>
<br>
<h4>Животный мир</h4>
<br>
<img src="../img/img113.jpg" class="Выборг">
<br>
<p>Животный мир Австралии не менее разнообразен и уникален. В большом количестве обитают утконос, ехидна, кенгуру, валлаби, кенгуровая крыса, опоссум, коала, вомбаты, австралийские бандикуты, сумчатая мышь. На острове Тасмания обитает редкий хищник — сумчатый дьявол. Одним из типично австралийских животных является дикая собака динго. Широко представлены рептилии.</p>
<br>
<p>В Австралии водится около 100 видов ядовитых змей. В прибрежных водах живет довольно большое количество опасных животных — около 70 видов акул, медуза австралийская (морская оса), морская змея, бородавчатая рыба и голубой осьминог.</p>
<br>
<p>Среди насекомых особенно примечательны гигантские термиты. На континенте обитает более 700 видов птиц, среди которых страус эму, казуар, кукабарра, лирохвост, попугаи, какаду, черные лебеди, тонкоклювый буревестник и многие другие.</p>
