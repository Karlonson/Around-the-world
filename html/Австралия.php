<h4>Полезная информация для туристов</h4>
<br>
<img src="../img/img116.jpg" class="Выборг">
<br>
<p>Жители страны дружественны, легко обучаемы, отличаются отрицанием авторитетов, любовью к острому слову. Костяк населения составили эмигранты из Англии и Ирландии.</p>
<br>
<p>С одной стороны, в стране сохраняется влияние английской культуры, проявляющейся иногда в сдержанности, чопорности, пуританстве, с другой стороны, многие наблюдатели отмечают сходство Австралии с Калифорнией, которое проявляется в жизнелюбии, свободе нравов, привычке большую часть времени проводить на открытом воздухе. Австралийцы приветливы с иностранцами. Они предельно лаконичны, считают краткость замечательным качеством.</p>
