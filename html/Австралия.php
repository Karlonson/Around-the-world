<h4>Банки и валюта</h4>
<br>
<img src="../img/img115.jpg" class="Выборг">
<br>
<p>Банки работают с понедельника по четверг с 9.30 до 16.00 (до 17.00 по пятницам), суббота и воскресенье - выходные дни (в некоторых штатах работают в субботу утром).</p>
<br>
<p>Обмен денег лучше производить в банках, их филиалах в аэропорту или обменных пунктах "Сингапур Мани Эксчейндж" (без комиссии), "Томас Кук" и "Америкэн Экспресс". Несколько менее выгодный курс в обменных пунктах отелей.</p>
<br>
<p>Принимаются все основные международные кредитные карты и туристические чеки (по карточкам "Виза" можно снимать не более $500 за раз, "Виза Премьер" - не более $2500), за их обналичивание взимается обычно до 15%. Действует обширная сеть банкоматов АТМ. В небольших частных магазинах, особенно в провинции, оплата пластиковыми карточками зачастую невозможна. </p>
<br>
<p>Дорожными чеками расплачиваться достаточно невыгодно, некоторые банки устанавливают за их обналичивание достаточно высокие комиссионные.</p>
<br>
<p>Австралийский доллар (AUD), равный 100 центам. В обращении находятся банкноты достоинством в 100, 50, 20, 10 и 5 долларов, монеты в 1, 2 доллара, а также в 50, 20, 10 и 5 центов.</p>
